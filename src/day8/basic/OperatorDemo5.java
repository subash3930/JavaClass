package day8.basic;

public class OperatorDemo5 {

	public static void main(String[] args) {
		//Increment /decrement operator
		//++/--
		// post and pre
		// a++,a-- and ++a,--a
		// this applies only when increment and decrement by one.
		int a =5;
		System.out.println(a);
		a++;
		System.out.println(a);
		++a;
		System.out.println(a);
		
		

	}

}
