package day8.basic;

public class OperatorDemo3 {

	public static void main(String[] args) {
		// 6.Relational operator >,<,>=,<=,!=
		// Result is true or false
		// used for logic building or condition.
		int a=5;
		int b=6;
		System.out.println(a>b);// false
		System.out.println(a>=b);// false
		System.out.println(a<=b);// true
		System.out.println(a<b);// true
		System.out.println(a!=b);// true
		

	}

}
